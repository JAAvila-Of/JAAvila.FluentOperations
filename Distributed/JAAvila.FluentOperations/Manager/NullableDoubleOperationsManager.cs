    /// <param name="caller">The caller expression name, captured automatically.</param>
    public NullableDoubleOperationsManager(double? value, string caller)
    {
        PrincipalChain = PrincipalChain<double?>.Get(value, caller);
        GlobalConfig.Initialize();
        SetManager(this);
    }

    /// <summary>
    /// Asserts that the nullable value has a non-null value.
    /// </summary>
    /// <param name="reason">An optional reason providing context for the assertion.</param>
    /// <returns>The current manager instance for method chaining.</returns>
    public NullableDoubleOperationsManager HaveValue(Reason? reason = null)
    {
        if (!OperationUtils.CheckOperationAllowed(Operations.NullableDouble.HaveValue))
        {
            return this;
        }

        ExecutionEngine<NullableDoubleOperationsManager, double?>
            .New(this)
            .WithOperation(NullableDoubleHaveValueValidator.New(PrincipalChain))
            .WithTemplate(
                (template, operation) =>
                    template
                        .WithSubject(PrincipalChain.GetSubject())
                        .WithResult(operation.ResultValidation)
                        .WithReason(reason?.ToString())
            )
            .Execute();

        return this;
    }

